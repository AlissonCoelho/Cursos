//CONVERSÕES

const n = 44;
const str = "44";

// converte os dois para o mesmo type por isso resultado é true
console.log(n == str);
//não faz nenhuma converão / compara os tipos
console.log(n === str);

//Concatena os dois valores
console.log(n + str);

//realiza a multiplicação dos dois valores
console.log(n * str);

//realiza a soma dos dois valores
console.log(n + Number(str));

